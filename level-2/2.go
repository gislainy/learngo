package main

import (
	"fmt"
)

func main() {
	a := 10 == 10
	b := 10 != 10
	c := 10 < 10
	d := 10 <= 10
	e := 10 > 10
	f := 10 >= 10
	fmt.Printf("a: %v\n", a)
	fmt.Printf("b: %v\n", b)
	fmt.Printf("c: %v\n", c)
	fmt.Printf("d: %v\n", d)
	fmt.Printf("e: %v\n", e)
	fmt.Printf("f: %v\n", f)
	
}
